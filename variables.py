# A program that greets people

firstName = "Elio"  # "Elio" is saved to the variable called firstName

print("Hello, " + firstName)  # Greeting a person

firstName = "Oliver"  # A new name is assigned to firstName

print("Hello, " + firstName)  # Greeting a person (again)


